export const buildAdvicePrompt = (params: {
  nickname: string
  sunSign: string
  moonSign: string
  honmeiStar: string
  mbti: string
  timing: string
  startMonth: number
  startYear: number
}) => {
  const { nickname, sunSign, moonSign, honmeiStar, mbti, timing, startMonth, startYear } = params
  const months = [0, 1, 2].map(i => {
    const m = ((startMonth - 1 + i) % 12) + 1
    const y = startYear + Math.floor((startMonth - 1 + i) / 12)
    return `${m}月（${y}年）`
  })
  return `以下の情報をもとに、${nickname}さんへの3ヶ月アドバイスをJSON形式で生成してください。

ニックネーム：${nickname}さん
太陽星座：${sunSign}
月星座：${moonSign}
本命星：${honmeiStar}
MBTI：${mbti}
転職タイミング：${timing}
対象月：${months[0]}・${months[1]}・${months[2]}

JSON配列のみ出力してください。`
}

