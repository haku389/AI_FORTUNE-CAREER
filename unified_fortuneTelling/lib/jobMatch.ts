export function calcJobMatch(
  mbti: string,
  motivation: string,
  roles: string[],
  industries: string[],
  sunSign?: string,
  moonSign?: string,
  honmeiStar?: string,
): { topJobs: JobMatch[]; topIndustries: IndustryMatch[]; agents: AgentMatch[] } {

  // 各職種の生スコアを計算（キャップなし）
  const scores: Record<JobName, number> = {} as Record<JobName, number>;

  for (const job of JOBS) {
    let score = 20; // ベーススコア

    for (const role of roles) {
      score += ROLE_JOB_SCORES[role]?.[job] ?? 0;
    }
    score += MBTI_JOB_BONUS[mbti]?.[job] ?? MBTI_JOB_BONUS['unknown'][job];
    score += MOTIVATION_BONUS[motivation]?.[job] ?? 0;
    if (sunSign)    score += ZODIAC_SUN_JOB_BONUS[sunSign]?.[job] ?? 0;
    if (moonSign)   score += ZODIAC_MOON_JOB_BONUS[moonSign]?.[job] ?? 0;
    if (honmeiStar) score += HONMEI_JOB_BONUS[honmeiStar]?.[job] ?? 0;

    scores[job] = score;
  }

  // 生スコアで順位を確定してから表示用スコアを割り当て
  const allScoreValues = Object.values(scores)
  const minRaw = Math.min(...allScoreValues)
  const rawRange = Math.max(...allScoreValues) - minRaw || 1

  const sortedEntries = (Object.entries(scores) as [JobName, number][]).sort(([, a], [, b]) => b - a)
  const top3Entries = sortedEntries.slice(0, 3)

  const raw1 = top3Entries[0]?.[1] ?? 0
  const raw2 = top3Entries[1]?.[1] ?? 0
  const raw3 = top3Entries[2]?.[1] ?? 0

  // rank1: rawスコアの全体比率から77〜95%の可変
  const proportional1 = Math.round(((raw1 - minRaw) / rawRange) * 18 + 77)
  const rank1Score = Math.max(77, Math.min(95, proportional1))

  // rank2: 64〜(rank1-5)%
  const proportional2 = Math.round(((raw2 - minRaw) / rawRange) * 16 + 64)
  const rank2Score = Math.max(64, Math.min(rank1Score - 5, proportional2))

  // rank3: 52〜(rank2-5)%
  const proportional3 = Math.round(((raw3 - minRaw) / rawRange) * 14 + 52)
  const rank3Score = Math.max(52, Math.min(rank2Score - 5, proportional3))

  const displayScores = [rank1Score, rank2Score, rank3Score]

  const topJobs: JobMatch[] = []
  for (let i = 0; i < top3Entries.length; i++) {
    const [job] = top3Entries[i]
    topJobs.push({ job, score: displayScores[i], reason: JOB_REASONS[job as JobName] })
  }

  // 業界ラベル
  const INDUSTRY_LABELS: Record<string, IndustryMatch> = {
    it:         { name: 'IT・Web・SaaS',         emoji: '💻' },
    consulting: { name: 'コンサル・経営企画',     emoji: '🎯' },
    healthcare: { name: '医療・ヘルスケア',       emoji: '🏥' },
    finance:    { name: '金融・不動産',           emoji: '💰' },
    creative:   { name: 'クリエイティブ・広告',   emoji: '🎨' },
    education:  { name: '教育・福祉',             emoji: '📚' },
    maker:      { name: 'メーカー・製造業',       emoji: '🏭' },
    hr:         { name: 'HR・人材・エージェント', emoji: '👥' },
    ec:         { name: 'EC・小売・サービス',     emoji: '🛒' },
    other:      { name: 'その他・幅広く',         emoji: '🌐' },
  };

  const topIndustries: IndustryMatch[] = industries.length > 0
    ? industries.slice(0, 3).map(ind => INDUSTRY_LABELS[ind] ?? { name: ind, emoji: '🌐' })
    : [{ name: 'IT・Web・SaaS', emoji: '💻' }, { name: 'コンサル・経営企画', emoji: '🎯' }];

  // エージェントプールを業界ベースで構築（最小2・最大4件）
  const seenNames = new Set<string>();
  const agentPool: AgentMatch[] = [];

  for (const ind of (industries.length > 0 ? industries : ['other'])) {
    for (const agent of (INDUSTRY_AGENTS[ind] ?? DEFAULT_AGENTS)) {
      if (!seenNames.has(agent.name)) {
        agentPool.push(agent);
        seenNames.add(agent.name);
      }
    }
  }

  // デフォルトで最低2件保証
  for (const agent of DEFAULT_AGENTS) {
    if (!seenNames.has(agent.name)) {
      agentPool.push(agent);
      seenNames.add(agent.name);
    }
  }

  const agents = agentPool.slice(0, 4);

  return { topJobs, topIndustries, agents };
}
